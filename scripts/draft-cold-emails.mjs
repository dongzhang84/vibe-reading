#!/usr/bin/env node
// Generate today's cold-reach-out txt for users we haven't emailed yet.
// Reads Auth + vr.books, subtracts addresses already in
// marketing/sent-emails.json, fills Template A or C, applies the same
// first-name rules we settled on, and writes:
//
//   marketing/cold-reachout-YYYY-MM-DD.txt
//
// Then `node scripts/send-cold-emails.mjs` (no --file needed; defaults to
// today's date) walks through that file and sends.
//
// Usage:
//   node scripts/draft-cold-emails.mjs                # all new users
//   node scripts/draft-cold-emails.mjs --max 20       # only 20 newest
//   node scripts/draft-cold-emails.mjs --tier A       # only uploaded users
//   node scripts/draft-cold-emails.mjs --tier C       # only signed-up-no-upload
//   node scripts/draft-cold-emails.mjs --since 2026-05-09  # only sign-ups on/after date

import { createClient } from '@supabase/supabase-js'
import {
  readFileSync,
  writeFileSync,
  existsSync,
  mkdirSync,
} from 'node:fs'
import { resolve, dirname } from 'node:path'

// ─── env ──────────────────────────────────────────────────────────
const env = readFileSync(resolve(process.cwd(), '.env.local'), 'utf8')
for (const line of env.split('\n')) {
  const m = line.match(/^([A-Z_]+)\s*=\s*(.*)$/)
  if (m) process.env[m[1]] = m[2].replace(/^["']|["']$/g, '')
}

// ─── args ─────────────────────────────────────────────────────────
const args = process.argv.slice(2)
function getArg(flag) {
  const i = args.indexOf(flag)
  return i >= 0 ? args[i + 1] : null
}
const MAX = parseInt(getArg('--max') ?? '999', 10)
const TIER_FILTER = getArg('--tier') // 'A' or 'C', else both
const SINCE = getArg('--since') // YYYY-MM-DD

// ─── load registry (skip-list) ────────────────────────────────────
const REGISTRY_PATH = resolve(process.cwd(), 'marketing/sent-emails.json')
const registry = existsSync(REGISTRY_PATH)
  ? JSON.parse(readFileSync(REGISTRY_PATH, 'utf8'))
  : {}
const alreadyEmailed = new Set(
  Object.keys(registry).filter((k) => !k.startsWith('_')).map((k) => k.toLowerCase()),
)

// ─── pull users from Auth + books ─────────────────────────────────
const db = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.SUPABASE_SERVICE_ROLE_KEY,
  { db: { schema: 'vr' } },
)

const { data: usersPage } = await db.auth.admin.listUsers({
  page: 1,
  perPage: 1000,
})
const users = usersPage.users

const { data: books } = await db
  .from('books')
  .select('owner_id, title, created_at')
  .order('created_at', { ascending: false })

const booksByOwner = new Map()
for (const b of books ?? []) {
  if (!b.owner_id) continue
  const prev = booksByOwner.get(b.owner_id) ?? 0
  booksByOwner.set(b.owner_id, prev + 1)
}

// ─── name-rendering rules (kept identical to 2026-05-09 batch) ───
function pickFirstNameRaw(u) {
  const m = u.user_metadata || {}
  const candidates = [
    m.given_name,
    m.first_name,
    typeof m.full_name === 'string' ? m.full_name.split(' ')[0] : null,
    typeof m.name === 'string' ? m.name.split(' ')[0] : null,
  ]
  for (const c of candidates) {
    if (typeof c === 'string' && c.trim().length > 0) return c.trim()
  }
  return ''
}

function isCJK(s) {
  return /[一-鿿]/.test(s)
}

function renderGreeting(rawName) {
  const FALLBACK = '亲爱的朋友，'
  if (!rawName) return FALLBACK
  // Chinese name: drop the first char (surname). Single char → no given
  // name remains → fallback to 亲爱的朋友.
  if (isCJK(rawName)) {
    if (rawName.length <= 1) return FALLBACK
    return `Hi ${rawName.slice(1)}，`
  }
  // Western/pinyin: capitalize first letter. Garbage handles fall back.
  if (rawName.length === 1) return FALLBACK
  // single-word "wang" alone (likely surname-only) → fallback
  if (rawName.toLowerCase() === 'wang') return FALLBACK
  // autogenerated junk like "Gmail-All", "F"
  if (/Gmail/i.test(rawName)) return FALLBACK
  const capped = rawName[0].toUpperCase() + rawName.slice(1)
  return `Hi ${capped}，`
}

// ─── load templates ───────────────────────────────────────────────
function loadTemplate(file) {
  const text = readFileSync(
    resolve(process.cwd(), 'marketing/templates', file),
    'utf8',
  )
  const subjMatch = text.match(/^SUBJECT:\s*(.+)$/m)
  if (!subjMatch) throw new Error(`Template ${file} missing SUBJECT: line`)
  const bodyStart = text.indexOf('\n\n', subjMatch.index) + 2
  const body = text.slice(bodyStart).trim()
  return { subject: subjMatch[1].trim(), body }
}

const TPL_A = loadTemplate('template-a-uploaded.txt')
const TPL_C = loadTemplate('template-c-signedup.txt')

// ─── build candidates list ───────────────────────────────────────
const candidates = []
for (const u of users) {
  const email = (u.email ?? '').toLowerCase()
  if (!email) continue
  if (alreadyEmailed.has(email)) continue
  if (SINCE && u.created_at < SINCE) continue

  const bookCount = booksByOwner.get(u.id) ?? 0
  const tier = bookCount > 0 ? 'A' : 'C'
  if (TIER_FILTER && TIER_FILTER !== tier) continue

  candidates.push({
    email: u.email,
    rawName: pickFirstNameRaw(u),
    tier,
    createdAt: u.created_at,
    bookCount,
  })
}

// Tier A first (most engaged), then Tier C; within each, newest first.
candidates.sort((a, b) => {
  if (a.tier !== b.tier) return a.tier === 'A' ? -1 : 1
  return (b.createdAt ?? '').localeCompare(a.createdAt ?? '')
})

const limited = candidates.slice(0, MAX)

// ─── render output txt ────────────────────────────────────────────
const today = new Date().toISOString().slice(0, 10)
const OUT_PATH = resolve(process.cwd(), `marketing/cold-reachout-${today}.txt`)
mkdirSync(dirname(OUT_PATH), { recursive: true })

const total = limited.length
const tierACount = limited.filter((c) => c.tier === 'A').length
const tierCCount = limited.filter((c) => c.tier === 'C').length

const tierLines = []
if (tierACount > 0) tierLines.push(`  TIER A (#1-${tierACount}) — 上传 ≥1 本，最 engaged`)
if (tierCCount > 0)
  tierLines.push(
    `  TIER C (#${tierACount + 1}-${total}) — 注册没上传，问"哪里卡住"`,
  )
const header = `VIBE READING — 中文 cold reach-out 邮件  (${today})
=====================================================

${total} 封新用户邮件（已自动跳过 ${alreadyEmailed.size} 个 sent-emails.json 里的）。
直接 copy → 粘贴 Gmail → 发送，或者跑 \`node scripts/send-cold-emails.mjs\`。

${tierLines.join('\n')}


`

const blocks = limited
  .map((c, i) => {
    const n = i + 1
    const tpl = c.tier === 'A' ? TPL_A : TPL_C
    const greeting = renderGreeting(c.rawName)
    // Templates contain "{{GREETING}}" as a placeholder on its own line —
    // substitute, don't prepend, otherwise the greeting appears twice.
    const renderedBody = tpl.body.replace('{{GREETING}}', greeting)
    return `─────────────── ${n}/${total} ───────────────
TO: ${c.email}
SUBJECT: ${tpl.subject}

${renderedBody}`
  })
  .join('\n\n\n')

writeFileSync(OUT_PATH, header + blocks + '\n')

console.log(`Wrote ${OUT_PATH}`)
console.log(`  ${total} new users (Tier A: ${tierACount}, Tier C: ${tierCCount})`)
console.log(`  Skipped (already in sent-emails.json): ${alreadyEmailed.size}`)
console.log(`\nNext: review the file, then`)
console.log(`  node scripts/send-cold-emails.mjs --dry-run`)
console.log(`  node scripts/send-cold-emails.mjs`)
